char		*ft_strchr(const char *src, int c)
{
	int j;

	j = 0;
	while (src[j] != '\0')
	{
		if (src[j] == (char)c)
			return ((char *)src + j);
		j++;
	}
	if (src[j] == '\0' && (char)c == '\0')
		return ((char *)src + j);
	return (0);
}
