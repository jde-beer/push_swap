char	*ft_strncat(char *s1, const char *s2, unsigned int n)
{
	unsigned int i;
	unsigned int z;

	i = 0;
	z = 0;
	while (s1[i] != '\0')
		i++;
	while (s2[z] != '\0' && z < n)
	{
		s1[i] = s2[z];
		i++;
		z++;
	}
	s1[i] = '\0';
	return (s1);
}
